from datetime import datetime


def add(a: float, b: float):
    """returns a + b."""
    return a + b


def substract(a: float, b: float):
    """returns a - b."""
    return a - b


def divide(a: float, b: float):
    """returns a / b."""
    return a / b


def multiply(a: float, b: float):
    """returns a * b."""
    return a * b


def datetime_now():
    """returns datetime now."""
    return datetime.now()
